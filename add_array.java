public class add_array {
     public static void main(String[]args){
        int sum=0;
        int mark[]=new int[4];
         mark[0]=10;
        mark[1]=20;
        mark[2]=30;
        mark[3]=40;
        for(int i=0;i<mark.length;i++){
            sum=sum+i;
        
        System.out.println(sum);
        }
     }
}
